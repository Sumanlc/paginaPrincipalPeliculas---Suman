let totalPokemonAcertados = 0;
let vidas = 3;
let racha = 0;
let seguirJugando;
let posicionPokemon;

function generarSiluetaPokemon() {
    let claves = Object.keys(siluetas);
    let numRandom = Math.floor(Math.random() * claves.length);
    let imgOutput = document.getElementById('pokemon');

    document.querySelector('figcaption').textContent = "";

    imgOutput.src = siluetas[claves[numRandom]];

    for (let key in siluetas) {
        if (siluetas.hasOwnProperty(key)) {
            if (siluetas[claves[numRandom]] === siluetas[key]) {
                posicionPokemon = key;
                delete siluetas[claves[numRandom]];
            }
        }
    }
    seguirJugando = false;
}

function anyadirImgPokemon(id) {
    let imgOutput = document.getElementById('pokemon');
    let clavesSiluetas = Object.keys(siluetas);
    let clavesPokemon = Object.keys(pokemon);

    if (clavesSiluetas.length === 0) {
        imgOutput.src = "../../img/juego/juegoPokemon/trofeo.webp";
        document.querySelector('figcaption').textContent = "¡Enhorabuena, has ganado!. Has entrado en el hall de la fama.";
    } else {
        let pokemonInput = document.getElementById(id).value;

        if (pokemonInput === posicionPokemon) {
            imgOutput.src = pokemon[pokemonInput];
            document.querySelector('figcaption').textContent = "¡Es " + posicionPokemon + "!";
            racha++;
            if (racha > 5) {
                totalPokemonAcertados += 2;
                let numRandom = Math.floor(Math.random() * clavesSiluetas.length);
                delete siluetas[clavesSiluetas[numRandom]];
                document.getElementById('racha').textContent = "¡Estás en racha! Tu racha es de: " + racha;
                document.getElementById('rachaImg').style.visibility = 'visible';
            } else {
                totalPokemonAcertados += 1;
            }
        } else {
            document.querySelector('figcaption').textContent = "Vaya... El Pokémon era " + posicionPokemon;
            document.getElementById('racha').textContent = "";
            document.getElementById('rachaImg').style.visibility = 'hidden';
            restarVidas();
            racha = 0;
            imgOutput.src = "../../img/juego/juegoPokemon/error.webp";
            if (vidas === 0) {
                document.body.style.background = 'red';
                imgOutput.setAttribute('src', "../../img/juego/juegoPokemon/game_over.webp");
            }
        }
        posicionPokemon = null;
        seguirJugando = true;
    }
    if (clavesSiluetas.length === 0 && totalPokemonAcertados === clavesPokemon.length - 1) totalPokemonAcertados += 1;
    document.getElementById('totalAciertos').textContent = totalPokemonAcertados;
}

function mostrarMenuAyuda() {
    let menuAyuda = document.querySelector('aside');

    if (menuAyuda.style.display === 'none') {
        menuAyuda.style.display = 'flex';
    } else {
        menuAyuda.style.display = 'none';
    }
}

function continuar() {
    if (seguirJugando && vidas > 0) {
        generarSiluetaPokemon();
    }
}

function nuevaPartida() {
    location.href = "../../html/juego/juegoPokemon.html";
}

function restarVidas() {
    let vida = document.getElementById('vida' + vidas);
    vida.setAttribute('src', "../../img/juego/juegoPokemon/corazonVacio.webp")
    vida.style.animationName = 'none'
    vidas--;
}