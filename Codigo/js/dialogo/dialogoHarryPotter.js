function cambiarEstilos(elemento) {
    elemento.style.fontFamily = 'Verdana, Geneva, Tahoma, sans-serif'; 
    elemento.style.color = '#ae8f4f'; 
}

function restaurarEstilos(elemento) {
    elemento.style.fontFamily = 'Lucida Sans, Lucida Grande, Geneva, Verdana, sans-serif'; 
    elemento.style.color = 'rgb(255, 255, 255)'; 
}

function restaurarNuevoEstilo(elemento) {
    elemento.style.fontFamily = 'Cambria, Cochin, Georgia, Times, Times New Roman , serif'; 
    elemento.style.color = 'rgb(255, 255, 255)'; 
}

